    if (n%3==0) and (n%5==0):
        return "FizzBuzz"
    else:
        if (n%3==0) and (n%5!=0):
            return "Fizz"
        else:
            if (n%5==0) and (n%3!=3):
                return "Buzz"
            else:
                return n

